//Main user control function
//includes functions from the SmartMotorLib.c, field_centric_control.c and motor_functions.c
task usercontrol()
{
	SmartMotorRun();
	startTask(lcdtask);
	SensorValue[FrontLeftEncoder] = 0;
	SensorValue[BackLeftEncoder] = 0;
	SensorValue[FrontRightEncoder] = 0;
	SensorValue[BackRightEncoder] = 0;

	startTask(launcher_listener);
	while(true){
		driveBase_listener();
		intake_listener();
		turntable_listener();
	}
}

//Initialize everything required before the autonomous starts
void pre_auton()
{
	bLCDBacklight = true;
	SmartMotorsInit();
	SmartMotorsAddPowerExtender(port5,port6,port7,port8);
	SmartMotorLinkMotors(port6,port3);
	SmartMotorLinkMotors(port6,port4);
	SmartMotorLinkMotors(port6,port5);
	SmartMotorsSetEncoderGearing(port6,3.0);
	SmartMotorPtcMonitorEnable();

}

//Run the autonomous function from the autonomous.c file
task autonomous()
{
	if(SensorValue[Potent] < redVal){
		//red
		runAutonRed();
	}
	else if(SensorValue[Potent] > blueVal){
		runAutonBlu();
	}
	else runProgrammingSkills();
}
